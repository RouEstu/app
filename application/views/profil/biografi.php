                            Peran penting yang ditunjukkan oleh para tokoh agama dan tokoh masyarakat dalam melakukan pembinaan mental spiritual telah mempertebal dan memperkuat sikap saling menghargai dan menghormati serta melahirkan pribadi-pribadi yang kokoh sehingga tidak mudah terprofokasi oleh hal-hal yang dapat merusak nilai-nilai kesatuan dan persatuan.
