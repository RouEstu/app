                            <br>

                            c. Bidang Prasarana Ekonomi
                            <br>
                            Bertumpu pada suatu kesadaran bahwa desa Jatisaba termasuk daerah agraris yang sebagian besar masyarakat mengandalkan sumber perekonomian-nya dari sektor pertanian dan perkebunan. Oleh karena itu pemerintah desa Jatisaba telah berupaya mengoptimalkan layanan dasar bidang pertanian dengan membangun beberapa buah bendungan (cekdam) guna memenuhi pasokan air untuk kebutuhan usaha pertanian baik tanaman padi maupun holtikultura.
                            <br>
                            Bendungan (cekdam) yang sudah berhasil dibangun antara lain : Bendungan kali Duren dan kali Mangir di wilayah RW.01, bendungan kali Situ di wilayah RW.02, bendungan kali Kemojing di wilayah RW.03, bendungan kali Warak di wilayah RW.04, bendungan kali Batur dan kali Sadang di wilayah RW.05.
                            Disamping membangun prasarana dasar bidang pertanian pihak pemerintah desa juga akan berupaya untuk membangun prasarana pemasaran hasil-hasil pertanian berupa pasar tradisional. Pembangunan prasarana pemasaran ini sudah diawali dengan adanya pembangunan kios desa yang diharapkan akan mampu mendorong gairah dan tumbuhnya usaha-usaha bidang yang lain sehingga laju pertumbuhan ekonomi diharapkan akan lebih cepat dibanding pada tahun-tahun sebelumnya.
                            <br>
                            d. Bidang Prasarana Sosial
                            <br>
                            1) Prasarana Ibadah
                            <br>
                            Dalam upaya menunjang pemenuhan kebutuhan kehidupan keagamaan menuju kepada terwujudnya peningkatan ketakwaan terhadap Tuhan Yang Maha Esa, seluruh warga masyarakat telah berhasil membangun sarana-sarana ibadah dalam berbagai ukuran dan bentuk sesuai dengan kapasitas kemampuan dan tingkat kebutuhan.
                            Dalam data inventarisasi fasilitas/prasarana ibadah tercatat ada 13 (tiga belas) buah masjid dan 21 (dua puluh satu) buah mushola yang tersebar merata di seluruh wilayah desa Jatisaba, atau menjadi desa dengan jumlah masjid terbanyak di wilayah Kecamatan Cilongok.
                            <br>
                            2) Prasarana Pendidikan
                            <br>
                            Pendidikan merupakan kebutuhan spiritual yang sangat berpengaruh terhadap kualitas intelektual (kognitif), aspek kepribadian (avektif) maupun sikap/perilaku (psikomotor) yang secara keseluruhan tercermin dalam kualitas Sumber Daya Manusia (SDM).
                            Dalam rangka membangun dan menyiapka ketersediaan Sumber Daya Manusia (SDM) yang berkualitas itu, di desa Jatisaba telah berdiri 5 (lima) buah Taman Kanak-Kanak dan 4 (empat) buah sekolah tingkat dasar yaitu SD dan Madrasah Ibtidaiyah. Selain itu berdiri pula lembaga pendidikan non formal berupa 1 (satu) buah madrasah diniyah (Madin) dan 7 (tujuh) buah Taman Pendidikan Al-Qurâan (TPA/TPQ).
