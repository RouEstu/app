                            1.1.2. Keadaan Sosial
                            <br>
                            Melalui upaya pemerataan pembangunan yang telah dilaksanakan di seluruh wilayah desa dalam kurun waktu 10 (sepuluh) tahun terakhir dan adanya pembinaan yang terus menerus, secara umum telah berhasil merubah kondisi sosial masyarakat dari suatu keterbelakangan menjadi lebih maju dalam beberapa hal, antara lain meningkatnya derajat kesehatan, meningkatnya kesadaran akan pentingnya aspek pendidikan, meningkatnya semangat kekeluargaan dan kegotong royongan serta pentingnya sikap bertoleransi, meningkatnya pemahaman akan hak dan kewajiban masing-masing selaku warga negara dan warga masyarakat, serta meningkatnya kewaspadaan terhadap segala kemungkinan yang dapat menimbulkan gangguan keamanan atau ancaman terhadap keutuhan Negara.
