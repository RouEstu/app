                            Pembangunan sarana air bersih juga telah dilaksanakan di semua tempat yang terdapat sumber mata air agar dapat dimanfaatkan sebesar-besarnya oleh masyarakat untuk memenuhi kebutuhan air minum dan memasak.
                            <br>
                            4) Sarana Telekomunkasi
                            <br>
                            Masuknya jaringan dan fasilitas telekomunikasi berupa telephon celular sangat membantu kelancaran komunikasi di tengah kehidupan masyarakat tanpa dibatasi lagi oleh dimensi jarak namun apabila tidak diwaspadai dapat berakibat adanya hal-hal negatif seperti kejahatan terselubung dan lain-lain.
                            <br>
                            1.1.3. Keadaan Ekonomi
                            Sebagian besar masyarakat desa Jatisaba bekerja sebagai buruh tani, buruh industri dan buruh bangunan dengan penghasilan yang rata-rata masih rendah sehingga mereka hanya mampu memenuhi kebutuhan konsumsi sehari-hari dan keadaan ini telah mendorong keinginan generasi mudanya untuk menjadi urban di kota-kota besar sehingga menyebabkan desa ini harus menerima suatu kenyataan akan kurangnya tenaga-tenaga profesional untuk mengolah dan memanfaatkan potensi sumber daya alam yang tersedia.
                            <br>
                            Perubahan mendasar yang telah dilakukan oleh pihak pemerintah desa adalah melakukan pembangunan infrastruktur dan membuka semua akses jalan menuju ke tempat-tempat yang mengandung potensi sumber daya alam agar dapat digali dan dimanfaatkan untuk meningkatkan derajat ekonomi warga.
                            Modernisasi dunia pertanian juga menjadi salah satu program unggulan dalam arah kebijakan pembangunan yang telah ditetapkan bersama antara Pemerintah Desa dan Badan Permusyawaratan Desa, disamping terus menerus memberikan dorongan terhadap tumbuh kembangnya industri-industri yang berbasis pertanian (perkebunan) serta menyediakan prasarana pemasaran berupa kios desa.
                            <br>
                            Indikator yang menunjukan terjadinya pertumbuhan perekonomian di desa Jatisaba adalah tumbuh kembangnya industri-industri kecil (Industri Rumah Tangga) yang semuanya berbahan baku hasil-hasil pertanian dan perkebunan seperti industri makanan, industri kayu (furniture), industri batu bata, dan produk-produk kerajinan bambu dalam berbagai bentuk dan fariasinya.
