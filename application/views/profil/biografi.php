                            Situasi dan kondisi kehidupan sosial yang mapan, sejuk dan damai disertai pola pikir yang konstruktif telah mendorong tumbuhnya prakarsa-prakarsa masyarakat dalam pelaksanaan pembangunan sehingga dapat mencapai suatu hasil yang lebih maksimal. Diantara hasil-hasil pembangunan yang sudah dapat dicapai adalah :
