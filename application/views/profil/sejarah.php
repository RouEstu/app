                        <p class="text-white blockquote">
                            Di dalam sebuah kisah yang sudah masyhur di tengah masyarakat diriwayatkan bahwa awal mula dibukanya desa Jatisaba adalah berkat kegigihan seorang prajurit Mataram bernama Raden Jati Anom alias Mbah Semutirta ketika mendapat perintah dari rajanya, Sultan Agung Hanyakrakusuma yang bernama asli Raden Mas Jatmika alias Raden Mas Rangsang, alias Prabu Pandita Sultan Agung Hanyakrakusuma atau yang bergelar Sutan Agung Senopati Ing Ngalaga Ngabdurrahman Sayyidin Panatagama, yaitu putra dari Prabu Hanyakrawati alias Mas Jolang alias Pangeran Seda Krapyak (Raja Mataram II) yang menggantikan ayahandanya Sultan Sutawijaya (Raja Mataram I) dengan Ratu Mas Adi Dyah Banawati putri dari Pangeran Benowo (Sultan Pajang II), yaitu perintah untuk membuka desa-desa baru terkait dengan rencana beliau yang akan melakukan suatu penyerangan besar-besaran terhadap benteng VOC Belanda di Batavia (Jayakarta).
                            <br>
                            Maka Raden Jati Anom melaksanakan perintah tersebut dengan membabad hutan Bandayudha yang terletak di sebalah Barat hutan Mangli dan dibuatnya menjadi lahan-lahan pertanian serta perkebunan untuk memenuhi kesiapan kebutuhan logistik (pangan) bagi pasukan Mataram yang berangkat ke Batavia pada tahun 1629, termasuk Raden Jati Anom sendiri.
                            Nama Jati Anom kemudian diabadikan menjadi nama desa sebagaimana penuturan para orang tua terdahulu, yaitu “Pesabane Raden Jati Anom anggenipun lelabetan dateng Kanjeng Sinuwun kawastanan desa Jatisaba”. Artinya, tempat dimana Raden Jati Anom melakukan aktifitas membabad (membuka) hutan dan menjadikannya sebuah desa maka tempat itu dinamakan desa Jatisaba
                            <br>
                            Sebelum masa-masa kemerdekaan, Ki Damin alias Ki Lurah Dipadirana mengawali kepemimpinan di desa Jatisaba sebagai Kepala Desa ke 1 (pertama). Ki Lurah Dipareja yang dikenal juga dengan sebutan Ki Lurah Kelat sebagai Kepala Desa ke 2 (dua). Kemudian dilanjutkan oleh Ki Lurah Dipasura sebagai Kepala Desa yang ke 3 (tiga) yang meninggal dunia pada tahun 1943, dan disusul kemudian oleh Ki Lurah Dipadiwirya sebagai Kepala Desa ke 4 (empat) yang meninggal dunia pada tahun 1956.
                            <br>
                            Pada masa Orde Lama hingga memasuki masa Orde Baru kepemimpinan desa Jatisaba dilaksanakan oleh Lurah Kartadimeja sebagai Kepala Desa ke 5 (lima). Kemudian melalui proses pemilihan yang demokratis pada tahun 1975 terpilih bapak Surat Hadi Sumarto sebagai Kepala Desa ke 6 (enam) yang di masa kepemimpinannya dapat dilihat hasil-hasil pembangunannya berupa gedung-gedung Sekolah Dasar (SD), Lapangan, Kantor Desa, pembukaan jalan-jalan desa, dll.
                            Selama ± 3 tahun desa Jatisaba pernah mengalami kekosongan jabatan Kepala Desa, yaitu pada sekitar tahun 1983 s/d 1985, maka kepemimpinan desa diserahkan kepada bapak Suparto yang ketika itu sedang menjabat sebagai Kepala Dusun (Kadus I). Pada masa itu dapat diraih suatu keberhasilan pembangunan antara lain berupa jembatan Kali Ente yang merupakan jalur penghubung antar desa, yaitu antara desa Jatisaba dan desa Pageraji melalui jalur barat (Dukuh Duren).
                            <br>
                            Akhir tahun 1985 kepemimpinan desa Jatisaba diserahkan kembali kepada bapak Surat Hadi Sumarto selaku Kepala Desa difinitif hingga beliau memasuki masa purna tugas pada tahun 1988. Beberapa bulan setelah itu, pelaksana Pemerintah Desa melakukan penyelenggaraan Pemilihan Kepala Desa yang dimenangkan oleh bapak Amin Supangat sebagai Kepala Desa ke 7 (tujuh).
                            <br>
                            Di masa kepemimpinan beliau dapat diraih banyak kemajuan seperti masuknya jaringan listrik (PLN) di wilayah Kadus I, pemugaran kantor desa dan dibangunnya pendopo atau balai pertemuan yang berbentuk Joglo, pembangunan jembatan penyeberangan ke grumbul Bulu Kuning, jembatan penyeberangan ke grumbul Dukuh Aren di wilayah Kadus I, jembatan penyeberangan ke grumbul Sidamukti dan jembatan penyeberangan yang menghubungkan grumbul Wanasari dengan grumbul Karangtengah di wilayah Kadus II.
                            <br>
                            Hal yang paling mendasar yang pernah dilakukannya adalah pemekaran wilayah RT. yang semula hanya ada 25 RT. menjadi 36 RT. serta dihidupkannya kembali sistem kerja dan kinerja lembaga-lembaga desa seperti LMD, LKMD, P2A, PKK, SKD, Karang Taruna, Kader Posyandu, dll.
                            <br>
                            Penyelenggaraan Pilkades tahun 1999, mengusung bapak Budiman sebagai Kepala Desa ke 8 (delapan) dan di masa kepemimpinannya juga banyak menghadirkan nuansa baru di dalam pola penyelenggaraan pemerintahan seiring dengan pemberlakuan Undang-Undang Nomor 32 Tahun 2004 tentang Pemerintahan Daerah, sebagaimana telah diubah dengan Undang-Undang Nomor 8 Tahun 2005 serta Peraturan Pemerintah Nomor 72 Tahun 2005 tentang Desa.
                            Penerapan sistem kontruksi beton untuk pembangunan jalan-jalan lentur (tanah labil) di tahun 2002 adalah merupakan bagian dari sisi gemilang yang telah mendatangkan dampak yang sangat positif bagi pemenuhan prasarana perhubungan karena seluruh jalur perhubungan dapat ditembus dan dilalui oleh kendaraan bermotor. Begitu pula dengan masuknya jaringan listrik (PLN) hampir di seluruh wilayah Kadus II sehingga merubah keadaan yang semula gelap menjadi terang benderang.
                            <br>
                            Peningkatan kualitas sarana ibadah, sarana pendidikan, sarana kesehatan lingkungan dan air bersih adalah merupakan target yang harus diwujudkan untuk memenuhi keinginan dan kebutuhan masyarakat, dan di dalam kenyataannya target itu bisa terwujud walau tidak seluruhnya karena terkendala oleh faktor keuangan. Demikian pula di bidang pembangunan mental spiritual melalui kegiatan kelompok-kelompok pengajian yang dilaksanakan secara rutin dan pengembangan kegiatan pendidikan agama oleh oramas-ormas keagamaan dengan mendirikan lembaga-lembaga pendidikan seperti TPA/TPQ dan Madrasah Diniyah.
                            <br>
                            Pemanfaatan potensi sumber daya alam dengan sistem pengelolaan yang cukup handal oleh tenaga-tenaga yang kapable, berdedikasi tinggi serta memiliki komitmen yang bagus terhadap kepentingan pembangunan desa adalah merupakan suatu terobosan yang terbukti mampu menjadikan kondisi desa ini secara fisik menjadi lebih baik dari masa-masa sebelumnya meskipun kurang didukung oleh lembaga pemerintahan yang lain, dalam hal ini adalah BPD yang dibentuk berdasarkan Perda Nomor 3 Tahun 2000.
                            <br>
                            Berakhirnya masa jabatan bapak Budiman pada bulan Mei 2007 yang ditindak lanjuti dengan penyelenggaraan Pemilihan Kepala Desa pada bulan Juni 2007 telah menempatkan bapak Slamet sebagai Kepala Desa Jatisaba ke 9 (sembilan). Ada beberapa hal yang cukup menggembirakan di masa ini, diantaranya ialah penerimaan Alokasi Dana Desa (ADD) yang bersumber dari Perimbangan Keuangan antara Pusat dan Daerah yang diterima oleh Daerah sesuai dengan pemberlakuan Undang-Undang Nomor 33 Tahun 2004 tentang Perimbangan Keuangan antara Pemerintah Pusat dan Pemerintah Daerah.
                            Pada Masa bapak Warid sebagai Kepala Desa Jatisaba ke 10 ( sepuluh ) Masuknya Dana Desa dari Pemerintah Pusat serta ADD yang semakin besar serta sumber bantuan- bantuan Kabupaten dan Propinsi juga merupakan bagian yang sangat besar arti dan manfaatnya bagi kepentingan percepatan pembangunan sehingga wilayah-wilayah yang tadinya terisolasi sekarang sudah bisa diakses dengan baik.
